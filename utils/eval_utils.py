# conversion utils
def numpy_to_pt(images):
    images = torch.from_numpy(images).float() * 2 - 1
    return images


def pt_to_numpy(images):
    images = images.clamp(-1, 1) / 2 + 0.5
    images = images.detach().cpu().numpy()
    return images


# main generation function
def generate_overlapping_volume(
    unet,
    scheduler,
    num_steps,
    seed,
    n_blocks,
    overlap,
    device,
    args,
    initial_context_gpu=None,
):
    """
    DOC Generated by LLM
    Generate a 3D volume by overlapping multiple blocks.
    This version uses inpainting to generate each block, respecting the overlapping region from previous block.

    Parameters:
    -----------
    unet : UNet3DModel
        The 3D UNet model with inpainting configuration
    scheduler : DDPMScheduler or similar
        The noise scheduler
    num_steps : int
        Number of denoising steps
    seed : int
        Random seed for generation
    n_blocks : int
        Number of overlapping blocks
    overlap : int
        Number of slices to overlap
    device : str or torch.device
        Device to run generation on
    args : dict
        Additional arguments for mask generation
    initial_context_gpu : torch.Tensor, optional
        Initial context (previous generation) to use for the first block (default: None)

    Returns:
    --------
    tuple: (cpu_parts, final_context_gpu)
        cpu_parts: list of torch.Tensor - parts of the volume on CPU
        final_context_gpu: tensor on GPU containing the final context (or None)
    """
    # check if the model is configured for inpainting mode
    assert hasattr(unet.config, "inpainting_mode") and unet.config.inpainting_mode, (
        "Model must be configured for inpainting mode."
    )

    if seed is None:
        seed = int(time.time())
    generator = torch.Generator(device=device).manual_seed(seed)

    sample_size = unet.config.sample_size

    if isinstance(sample_size, int):
        D = H = W = sample_size
    else:
        D, H, W = sample_size
    # Use the original channel count from the model config
    C = unet.config.in_channels

    step = D - overlap
    total_depth = step * n_blocks + overlap
    cpu_parts = []
    context_latent = initial_context_gpu  # Context should be the latent state from the previous block's end
    unet.eval()  # Ensure eval mode
    scheduler.set_timesteps(num_steps)

    mask_generator = MaskGenerator3D(args, num_channels=1)  # Pass the full args object
    mask = mask_generator([1, 1, D, H, W]).to(device)  # Generate mask for a single instance, batch dim 1

    for i in tqdm(range(n_blocks), desc="Generating Inpainting Blocks"):
        # init latents for the entire block on GPU
        latents = torch.randn((1, C, D, H, W), generator=generator, device=device)

        # get the known latent context
        known_latent_part = context_latent

        # run diffusion sampling using inpainting logic
        with torch.no_grad():
            for t in scheduler.timesteps.to(device):
                # prepare U-Net input: [current_latents, mask, masked_known_latent]
                # create masked_known_latent use context if available
                masked_known_latent = torch.zeros_like(latents)
                if known_latent_part is not None and overlap > 0:
                    masked_known_latent[:, :, :overlap, :, :] = known_latent_part

                # For inpainting UNet: concatenate [noisy_latents, mask, masked_known_latent]
                unet_input = torch.cat([latents, mask, masked_known_latent], dim=1)

                # predict noise
                t_input = t.repeat(1)  # batch size 1
                noise_pred = unet(unet_input, t_input, return_dict=False)[0]

                # scheduler step to get prev_sample (prediction of x_{t-1})
                prev_sample = scheduler.step(noise_pred, t, latents).prev_sample

                # inpainting step blend known and predicted regions
                if known_latent_part is not None and overlap > 0:
                    # Noise the known latent part (x0 context) to timestep t-1
                    # Need to sample noise corresponding to the step
                    # noise = torch.randn_like(latents)

                    if known_latent_part.dim() == 4:  # Shape (C, D_overlap, H, W)
                        known_latent_part_b = known_latent_part.unsqueeze(0)  # Shape (1, C, D_overlap, H, W)
                    else:
                        known_latent_part_b = known_latent_part  # Assume already has batch dim

                    # Sample noise matching the shape of the known part
                    noise_for_known_part = torch.randn_like(known_latent_part_b)

                    # Get the timestep t-1 (or 0 if t is 0)
                    prev_t = t - scheduler.config.num_train_timesteps // scheduler.num_inference_steps
                    if prev_t < 0:
                        prev_t = t  # Handle edge case if t=0

                    known_latent_noisy_prev_t = scheduler.add_noise(
                        known_latent_part_b, noise_for_known_part, prev_t.unsqueeze(0)
                    )

                    # Start with the full prediction
                    combined_latents = prev_sample.clone()
                    # Overwrite the known (overlapping) region with the correctly noised known part
                    combined_latents[:, :, :overlap, :, :] = known_latent_noisy_prev_t
                    latents = combined_latents  # Update latents for the next step

                else:
                    latents = prev_sample

        block = latents.squeeze(0)
        if i == 0:
            cpu_parts.append(block[..., : (step + overlap), :, :].cpu())
        else:
            cpu_parts.append(block[..., overlap : (overlap + step), :, :].cpu())

        if overlap > 0:
            context_latent = block[..., D - overlap : D, :, :].clone().unsqueeze(0)  # Add batch dim back
        else:
            context_latent = None

        del block, unet_input, noise_pred, prev_sample
        if "masked_known_latent" in locals():
            del masked_known_latent
        torch.cuda.empty_cache()

    return cpu_parts, None


def generate_single_volume(
    unet,
    scheduler,
    num_steps,
    seed,
    batch_size,
    device,
    args,
    min_bw_ratio=0.0,
    max_retries=5,
    progress_callback=None,
):
    """
    DOC Generated by LLM
    Samples a batch of 3D volume blocks based on the unet's config.
    Retries generation for individual volumes in the batch if they don't meet min_bw_ratio.
    Optionally updates progress via a callback function after each volume.
    Returns a list of NumPy arrays, each of shape (D, H, W) with values in [0,1].

    Parameters:
    -----------
    unet : UNet3DModel or UNet2DModel
        The UNet model
    scheduler : DDPMScheduler or similar
        The noise scheduler
    num_steps : int
        Number of denoising steps
    seed : int
        Random seed for generation
    batch_size : int
        Number of volumes to generate
    device : str or torch.device
        Device to run generation on
    args : dict
        Additional arguments for mask generation
    min_bw_ratio : float, default=0.0
        Minimum required ratio of dark voxels (<=0.5)
    max_retries : int, default=5
        Maximum number of retries if min_bw_ratio is not met
    progress_callback : function, optional
        Function to call after each volume is processed

    Returns:
    --------
    list of NumPy arrays
    """

    # get the block size from the unet config
    sample_size = unet.config.sample_size
    if isinstance(sample_size, int):
        D = H = W = sample_size
    else:
        D, H, W = sample_size
    C = unet.config.in_channels
    image_shape = (batch_size, C, D, H, W)

    mask = None
    model_is_inpainting = hasattr(unet.config, "inpainting_mode") and unet.config.inpainting_mode

    if model_is_inpainting:
        if hasattr(args, "mask_type") and args.mask_type is not None and args.mask_type != "none":
            logger.info(f"Model is inpainting. Using mask_type: {args.mask_type} for single volume generation.")
            mask_generator = MaskGenerator3D(args, num_channels=1)
            mask = mask_generator([batch_size, 1, D, H, W]).to(device)
        else:
            logger.warning(
                f"Model is inpainting, but no valid mask_type specified in args (mask_type: {getattr(args, 'mask_type', 'Not Set')}). "
                f"Proceeding without mask. This might lead to unexpected behavior or errors if the inpainting model strictly requires mask channels."
            )
    else:  # Model is NOT inpainting (standard/unconditional)
        if hasattr(args, "mask_type") and args.mask_type is not None and args.mask_type != "none":
            logger.warning(
                f"Model is NOT inpainting (unconditional). Ignoring user-specified or default args.mask_type ('{args.mask_type}') for single volume generation, as unconditional models do not use masks."
            )
        # mask remains None, as intended for an unconditional model

    # process the batch, applying retry logic per volume
    volumes_np_0_1 = []
    base_generator = torch.Generator(device=device).manual_seed(seed)

    latents = torch.randn(image_shape, generator=base_generator, device=device)
    scheduler.set_timesteps(num_steps)
    unet.eval()
    with torch.no_grad():
        for t in scheduler.timesteps:
            if mask is not None:
                masked_input = torch.zeros_like(latents)
                model_input = torch.cat([latents, mask, masked_input], dim=1)
            else:
                model_input = latents

            t_input = t.to(device).repeat(batch_size)
            noise_pred = unet(model_input, t_input, return_dict=False)[0]
            latents = scheduler.step(noise_pred, t, latents, generator=base_generator).prev_sample

    for i in range(batch_size):
        volume_meets_criteria = False
        retries = 0
        last_clipped_vol = None
        current_seed_offset = 0  # to prevent the same seed for each volume

        while not volume_meets_criteria and retries <= max_retries:
            if retries > 0:
                print(f"  Retrying volume {i + 1}/{batch_size} (attempt {retries}/{max_retries})...")
                volume_seed = int.from_bytes(os.urandom(8), "big")

                volume_generator = torch.Generator(device=device).manual_seed(volume_seed)
                single_latent = torch.randn((1, C, D, H, W), generator=volume_generator, device=device)

                scheduler.set_timesteps(num_steps)
                with torch.no_grad():
                    for t in scheduler.timesteps.to(device):
                        t_input = t.repeat(1)
                        noise_pred = unet(single_latent, t_input, return_dict=False)[0]
                        single_latent = scheduler.step(noise_pred, t, single_latent).prev_sample

                block_cpu = single_latent[0].cpu().squeeze(0)  # (D, H, W)
                del single_latent
            else:
                block_cpu = latents[i].cpu().squeeze(0)  # (D, H, W)

            vol_np = block_cpu.detach().numpy()
            scaled_vol = (vol_np + 1.0) / 2.0
            clipped_vol = np.clip(scaled_vol, 0.0, 1.0)

            last_clipped_vol = clipped_vol

            bw_ratio = np.mean(clipped_vol <= 0.5)
            print(f"  Volume {i + 1}/{batch_size} BW ratio: {bw_ratio:.4f}")

            if bw_ratio >= min_bw_ratio or min_bw_ratio <= 0:
                volume_meets_criteria = True
                volumes_np_0_1.append(clipped_vol)
            else:
                retries += 1
                del vol_np, scaled_vol, clipped_vol, block_cpu
                torch.cuda.empty_cache()

        if not volume_meets_criteria:
            print(
                f"Warning: Minimum BW ratio ({min_bw_ratio}) not met for volume index {i} after {max_retries} retries. Appending last generated volume."
            )
            if last_clipped_vol is not None:
                volumes_np_0_1.append(last_clipped_vol)
            else:
                print(f"Error: Could not append volume for index {i} as no volume was generated.")

        if progress_callback:
            progress_callback()

    del latents
    torch.cuda.empty_cache()
    return volumes_np_0_1


def generate_cpu_overlapping_volume(
    unet,
    scheduler,
    num_steps,
    seed,
    n_blocks,
    overlap,
    device,
    args,
    progress_callback=None,
    min_bw_ratio=0.0,
    max_retries=5,
):
    """
    DOC Generated by LLM
    Fallback CPU-based overlapping: generate each block independently and blend on CPU.
    Optionally updates progress via a callback function after each block.
    Ensures individual blocks meet min_bw_ratio via retries.
    Returns a NumPy array of shape (total_depth, H, W).

    Parameters:
    -----------
    unet : UNet3DModel or UNet2DModel
        The UNet model
    scheduler : DDPMScheduler or similar
        The noise scheduler
    num_steps : int
        Number of denoising steps
    seed : int
        Random seed for generation
    n_blocks : int
        Number of blocks to generate
    overlap : int
        Number of overlapping slices between blocks
    device : str or torch.device
        Device to run generation on
    args : dict
        Additional arguments for mask generation
    progress_callback : function, optional
        Function to call after each block is processed
    min_bw_ratio : float, default=0.0
        Minimum required ratio of dark voxels (<=0.5)
    max_retries : int, default=5
        Maximum number of retries if min_bw_ratio is not met

    Returns:
    --------
    NumPy array of shape (total_depth, H, W)
    """
    volumes = []
    # Generate each block independently
    for i in range(n_blocks):
        block_seed = seed + i
        # Generate a single block as numpy - pass progress_callback=None to prevent inner tqdm
        # Pass down the ratio and retry parameters
        vol_list = generate_single_volume(
            unet,
            scheduler,
            num_steps,
            block_seed,
            1,
            device,
            args=args,
            min_bw_ratio=min_bw_ratio,
            max_retries=max_retries,
            progress_callback=None,
        )

        if not vol_list:
            print(f"Warning: Failed to generate block {i + 1}/{n_blocks} in CPU overlapping mode after retries.")
            continue

        vol = vol_list[0]
        volumes.append(vol)
        if progress_callback:
            progress_callback()

    if not volumes:
        print("Warning: No volumes generated in CPU overlapping mode.")
        return None
    full = volumes[0]
    for idx in range(1, n_blocks):
        next_vol = volumes[idx]
        if overlap > 0:
            # Blend overlap
            blend = (full[-overlap:] + next_vol[:overlap]) / 2.0
            full = np.concatenate([full[:-overlap], blend, next_vol[overlap:]], axis=0)
        else:
            full = np.concatenate([full, next_vol], axis=0)
    return full


## inpainting version
def batch_inpaint_junctions(
    full_volume_pt,
    junction_infos,
    inpainting_pipeline,
    num_inference_steps,
    device,
    args,
    seed,
    use_gap_filling=False,
    output_dir=None,
    inpaint_iteratively=False,
    inpaint_iterations=3,
    inpaint_region_size_ratio=0.3,
    axis=0,  # New parameter: axis along which to inpaint
    max_batch_size=None,  # New parameter: maximum batch size to avoid OOM
):
    """
    Batch process multiple junctions for inpainting to leverage GPU parallelization.
    Now includes automatic batch chunking to avoid CUDA OOM errors.
    
    Args:
        full_volume_pt: The full volume tensor on GPU
        junction_infos: List of junction information dictionaries
        inpainting_pipeline: Loaded inpainting pipeline
        num_inference_steps: Number of diffusion steps
        device: GPU device
        args: Arguments containing mask configuration
        seed: Random seed base
        use_gap_filling: Whether using gap filling mode
        output_dir: Debug output directory
        inpaint_iteratively: Whether to use iterative inpainting
        inpaint_iterations: Number of iterations for iterative inpainting
        inpaint_region_size_ratio: Size ratio for inpainting region
        axis: Axis along which to inpaint
        max_batch_size: Maximum batch size to avoid OOM (auto-detect if None)
    
    Returns:
        Updated full_volume_pt with inpainted junctions
    """
    if not junction_infos:
        return full_volume_pt
    
    # Auto-detect max batch size based on available GPU memory if not specified
    if max_batch_size is None:
        # Conservative estimate: start with a reasonable batch size
        # Can be adjusted based on GPU memory and volume size
        total_junctions = len(junction_infos)
        if total_junctions <= 10:
            max_batch_size = total_junctions
        elif total_junctions <= 50:
            max_batch_size = 20
        elif total_junctions <= 100:
            max_batch_size = 15
        else:
            max_batch_size = 10  # Conservative for very large batches
    
    # Split junctions into chunks if needed
    if len(junction_infos) > max_batch_size:
        logger.info(f"Splitting {len(junction_infos)} junctions into chunks of {max_batch_size} to avoid OOM")
        
        # Process junctions in chunks
        for chunk_start in range(0, len(junction_infos), max_batch_size):
            chunk_end = min(chunk_start + max_batch_size, len(junction_infos))
            chunk_infos = junction_infos[chunk_start:chunk_end]
            chunk_seed = seed + chunk_start  # Different seed for each chunk
            
            logger.info(f"Processing junction chunk {chunk_start//max_batch_size + 1}/{(len(junction_infos) + max_batch_size - 1)//max_batch_size}: "
                       f"junctions {chunk_start}-{chunk_end-1} ({len(chunk_infos)} junctions)")
            
            # Recursively call with smaller batch
            full_volume_pt = batch_inpaint_junctions(
                full_volume_pt=full_volume_pt,
                junction_infos=chunk_infos,
                inpainting_pipeline=inpainting_pipeline,
                num_inference_steps=num_inference_steps,
                device=device,
                args=args,
                seed=chunk_seed,
                use_gap_filling=use_gap_filling,
                output_dir=output_dir,
                inpaint_iteratively=inpaint_iteratively,
                inpaint_iterations=inpaint_iterations,
                inpaint_region_size_ratio=inpaint_region_size_ratio,
                axis=axis,
                max_batch_size=max_batch_size,  # Maintain the same batch size
            )
            
            # Clear cache between chunks
            torch.cuda.empty_cache()
        
        return full_volume_pt
    
    # Original batch processing logic (for batches <= max_batch_size)
    inpainting_unet = inpainting_pipeline.unet
    inpainting_scheduler = inpainting_pipeline.scheduler
    inpainting_scheduler.set_timesteps(num_inference_steps)
    inpainting_unet.eval()
    
    # Extract all junction regions and create masks
    batch_image_slices = []
    batch_masks = []
    batch_regions = []
    
    logger.info(f"Preparing batch of {len(junction_infos)} junctions for parallel inpainting")
    
    valid_junction_infos = []
    for i, junction_info in enumerate(junction_infos):
        region_start_d = junction_info['region_start_d']
        region_end_d = junction_info['region_end_d']
        junction_center_d = junction_info['junction_center_d']
        gap_size = junction_info.get('gap_size', 0)
        
        # Check if region is valid
        region_depth = region_end_d - region_start_d
        if region_depth <= 0:
            logger.warning(f"Skipping junction {i} with invalid region depth: {region_depth} (start={region_start_d}, end={region_end_d})")
            continue
            
        # Extract junction slice based on axis
        if axis == 0:
            image_slice = full_volume_pt[0, :, region_start_d:region_end_d, :, :].clone()
        elif axis == 1:
            image_slice = full_volume_pt[0, :, :, region_start_d:region_end_d, :].clone()
        elif axis == 2:
            image_slice = full_volume_pt[0, :, :, :, region_start_d:region_end_d].clone()
        else:
            raise ValueError(f"Unsupported axis: {axis}")
        
        # Validate slice dimensions
        if image_slice.numel() == 0:
            logger.warning(f"Skipping junction {i} with empty slice dimensions: {image_slice.shape}")
            continue
            
        batch_image_slices.append(image_slice)
        batch_regions.append((region_start_d, region_end_d))
        valid_junction_infos.append(junction_info)
        
        # Create mask for this junction
        junction_local_center = junction_center_d - region_start_d
        
        if use_gap_filling:
            mask_width = gap_size
        else:
            mask_width = int(region_depth * inpaint_region_size_ratio)
        
        mask_start = max(0, junction_local_center - mask_width // 2)
        mask_end = min(region_depth, mask_start + mask_width)
        
        mask = torch.zeros_like(image_slice)
        if axis == 0:
            mask[:, mask_start:mask_end, :, :] = 1.0
        elif axis == 1:
            mask[:, :, mask_start:mask_end, :] = 1.0
        elif axis == 2:
            mask[:, :, :, mask_start:mask_end] = 1.0
        batch_masks.append(mask)
    
    # If no valid junctions, return early
    if not batch_image_slices:
        logger.info("No valid junctions found for batch inpainting")
        return full_volume_pt
    
    # Stack into batch tensors
    batch_image_slices = torch.stack(batch_image_slices, dim=0)  # (B, C, D, H, W)
    batch_masks = torch.stack(batch_masks, dim=0)  # (B, C, D, H, W)
    batch_mask_unet_input = batch_masks[:, 0:1, ...].to(device)  # (B, 1, D, H, W)
    
    # Move to device
    batch_image_slices = batch_image_slices.to(device)
    batch_masks = batch_masks.to(device)
    
    # Generate seeds for each junction
    batch_generators = []
    for i in range(len(junction_infos)):
        junction_seed = seed + i * 1000
        generator = torch.Generator(device=device).manual_seed(junction_seed)
        batch_generators.append(generator)
    
    # Main batch inpainting process
    logger.info(f"Starting batch inpainting for {len(junction_infos)} junctions")
    
    try:
        timesteps = inpainting_scheduler.timesteps
        
        # Create initial noise for entire batch
        batch_latents = torch.randn(batch_image_slices.shape, device=device)
        
        # Create masked images for entire batch
        batch_masked_images = batch_image_slices * (1.0 - batch_masks) - batch_masks
        
        # Scale initial noise if needed
        if hasattr(inpainting_scheduler, "init_noise_sigma"):
            batch_latents = batch_latents * inpainting_scheduler.init_noise_sigma
        
        with torch.inference_mode():
            for i_step, t in enumerate(tqdm(timesteps, desc="Batch Inpainting", leave=False)):
                # Prepare batch input for UNet
                t_input = t.repeat(batch_image_slices.shape[0])  # Batch size
                
                # For inpainting UNet: concatenate [noisy_latents, mask, masked_images]
                unet_input = torch.cat([batch_latents, batch_mask_unet_input, batch_masked_images], dim=1)
                
                # Batch predict noise
                noise_pred = inpainting_unet(unet_input, t_input, return_dict=False)[0]
                
                # Batch scheduler step
                step_output = inpainting_scheduler.step(noise_pred, t, batch_latents)
                x_prev_denoised_candidate = step_output.prev_sample
                
                # RePaint-like guidance for batch
                if i_step < len(timesteps) - 1:
                    prev_t = timesteps[i_step + 1]
                    noise_for_gt_conditioning = torch.randn(
                        batch_image_slices.shape, device=device, dtype=batch_image_slices.dtype
                    )
                    
                    # Add noise to original images
                    x_prev_noised_known_gt = inpainting_scheduler.add_noise(
                        batch_image_slices, noise_for_gt_conditioning, prev_t.unsqueeze(0)
                    )
                    
                    # Composite: use inpainted prediction for masked areas, noisy GT for known areas
                    batch_latents = x_prev_denoised_candidate * batch_masks + x_prev_noised_known_gt * (1.0 - batch_masks)
                else:
                    # Last step: composite final x0 prediction
                    batch_latents = x_prev_denoised_candidate * batch_masks + batch_image_slices * (1.0 - batch_masks)
        
        # Place results back into full volume based on axis
        for i, (region_start_d, region_end_d) in enumerate(batch_regions):
            if axis == 0:
                full_volume_pt[0, :, region_start_d:region_end_d, :, :] = batch_latents[i]
            elif axis == 1:
                full_volume_pt[0, :, :, region_start_d:region_end_d, :] = batch_latents[i]
            elif axis == 2:
                full_volume_pt[0, :, :, :, region_start_d:region_end_d] = batch_latents[i]
        
        logger.info(f"Batch inpainting completed for {len(valid_junction_infos)} valid junctions out of {len(junction_infos)} total")
        
        # Save debug outputs if requested
        if output_dir:
            debug_dir = Path(output_dir) / f"batch_inpainting_debug_seed{seed}"
            debug_dir.mkdir(parents=True, exist_ok=True)
            for i in range(len(valid_junction_infos)):
                result_np = pt_to_numpy(batch_latents[i])
                np.save(debug_dir / f"batch_junction_{i:02d}.npy", result_np)
        
    except Exception as e:
        logger.error(f"Error in batch inpainting: {e}")
        import traceback
        traceback.print_exc()
        # Fall back to sequential processing
        return full_volume_pt
    
    # Cleanup
    del batch_image_slices, batch_masks, batch_latents, batch_masked_images
    torch.cuda.empty_cache()
    
    return full_volume_pt


def generate_stitched_volume_with_inpainting(
    base_unet,
    base_scheduler,
    inpainting_pipeline,  # Expect a loaded Diffusers pipeline
    num_inference_steps,
    seed,
    n_blocks,
    overlap,
    device,
    args,
    output_dir,  # for debugging intermediate blocks
    generation_batch_size=1,
    strength=1.0,  # Strength close to 1 means using more steps for inpainting
    inpaint_region_size_ratio=0.3,  # Size of inpainting region as ratio of process region
    inpaint_iteratively=False,  # Whether to inpaint in smaller iterations
    inpaint_iterations=3,  # Number of iterations for iterative inpainting
    threshold_value=None,  # Value to threshold the final volume at (None = no thresholding)
):
    """
    DOC Generated by LLM
    Generates a volume by creating independent blocks and stitching seams with a dedicated inpainting pipeline.
    This version assumes the inpainting_pipeline contains a UNet trained for inpainting
    (e.g., extra input channels) but the pipeline itself is a DDPMPipeline or similar.

    Args:
        base_unet: UNet model for initial block generation.
        base_scheduler: Scheduler for initial block generation.
        inpainting_pipeline: Loaded Diffusers pipeline containing the inpainting UNet and scheduler.
        num_inference_steps: Number of steps for both generation and inpainting.
        seed: Base random seed.
        n_blocks: Number of blocks to generate along the depth axis.
        overlap: Number of slices to overlap between blocks (defines seam width).
        device: PyTorch device.
        output_dir: Directory to save intermediate blocks (optional, for debugging).
        generation_batch_size: Batch size for generating initial blocks.
        # guidance_scale: Guidance scale not applicable here.
        strength: Controls how much noise is added before inpainting. 1.0 means full denoising.
        inpaint_region_size_ratio: Size of inpainting region as ratio of process region.
        inpaint_iteratively: Whether to inpaint in smaller iterations.
        inpaint_iterations: Number of iterations for iterative inpainting.
        threshold_value: Value to threshold the final volume at (None = no thresholding).

    Returns:
        A NumPy array representing the final stitched volume [0, 1], or None if generation fails.
    """
    logger.info(f"Starting separate inpainting stitching: {n_blocks=}, {overlap=}, {seed=}")
    torch.cuda.empty_cache()

    # inital blocks
    sample_size = base_unet.config.sample_size
    if isinstance(sample_size, int):
        D, H, W = sample_size, sample_size, sample_size
    else:
        D, H, W = sample_size
    logger.info(f"Base block dimensions: D={D}, H={H}, W={W}")

    all_blocks_np = []
    num_gen_batches = (n_blocks + generation_batch_size - 1) // generation_batch_size
    current_seed = seed
    pbar_gen = tqdm(total=n_blocks, desc="Generating Initial Blocks")

    for i in range(num_gen_batches):
        batch_seed = current_seed + i * generation_batch_size
        bs = min(generation_batch_size, n_blocks - len(all_blocks_np))
        if bs <= 0:
            break

        logger.debug(f"Generating block batch {i + 1}/{num_gen_batches} with size {bs} and seed {batch_seed}")
        try:
            # Create a temporary args for unconditional block generation for Stage 1
            temp_args_for_block_gen = argparse.Namespace(**vars(args))
            temp_args_for_block_gen.mask_type = "none"  # Ensure unconditional generation

            block_batch_np = generate_single_volume(
                base_unet,
                base_scheduler,
                num_inference_steps,
                batch_seed,
                bs,
                device,
                args=temp_args_for_block_gen,  # Pass temporary args
                min_bw_ratio=0.0,  # Assuming we don't need strict bw_ratio for initial blocks here
                max_retries=0,  # Or handle retries if needed for initial blocks
                progress_callback=lambda: pbar_gen.update(1),
            )
            if block_batch_np is None or len(block_batch_np) != bs:
                raise RuntimeError(f"generate_single_volume failed for batch {i + 1}")
            all_blocks_np.extend(block_batch_np)
        except Exception as e:
            logger.error(f"Error generating block batch {i + 1}: {e}")
            pbar_gen.close()
            return None

    pbar_gen.close()
    if len(all_blocks_np) != n_blocks:
        logger.error(f"Failed to generate the required number of blocks ({len(all_blocks_np)}/{n_blocks})")
        return None
    logger.info(f"Successfully generated {n_blocks} initial blocks.")

    if output_dir:
        debug_dir = Path(output_dir) / f"intermediate_blocks_seed{seed}"
        debug_dir.mkdir(parents=True, exist_ok=True)
        for i, block in enumerate(all_blocks_np):
            np.save(debug_dir / f"block_{i:03d}.npy", block)
        logger.info(f"Saved intermediate blocks to {debug_dir}")

    # Check if we should use gap-filling mode (true gaps) or overlapping mode (seams)
    use_gap_filling = hasattr(args, "mask_type") and args.mask_type == "gap_filling_compatible"

    if use_gap_filling:
        # Gap-filling mode: create true gaps between blocks
        gap_size = overlap  # In gap mode, "overlap" parameter defines gap size
        step = D + gap_size  # Blocks are separated by gap_size
        total_depth = n_blocks * D + (n_blocks - 1) * gap_size
        logger.info(f"Gap-filling mode: {total_depth=} ({n_blocks} blocks, {D=}, {gap_size=}, {step=})")
    else:
        # Overlapping mode: blocks overlap and seams are inpainted
        step = D - overlap
        total_depth = D + (n_blocks - 1) * step
        logger.info(f"Overlapping mode: {total_depth=} ({n_blocks} blocks, {D=}, {overlap=}, {step=})")

    C = base_unet.config.in_channels
    logger.info(f"Using {C} channels based on base_unet config.")
    full_volume_pt = torch.zeros((1, C, total_depth, H, W), dtype=torch.float32, device="cpu")

    first_block_np = all_blocks_np[0]
    if first_block_np.ndim == 3:
        first_block_np = np.expand_dims(first_block_np, axis=0)

    total_inpaint_channels = inpainting_pipeline.unet.config.in_channels
    if (total_inpaint_channels - 1) % 2 != 0:
        logger.warning(
            f"Inpainting UNet total input channels ({total_inpaint_channels}) doesn't fit 2*C+1 structure. Assuming C=1."
        )
        inpaint_C = 1
    else:
        inpaint_C = (total_inpaint_channels - 1) // 2
    logger.info(
        f"Inpainting UNet expects {inpaint_C} data channels (derived from total channels: {total_inpaint_channels})."
    )

    if first_block_np.shape[0] == 1 and C == 1 and inpaint_C > 1:
        logger.warning(f"Repeating single channel block to {inpaint_C} channels for inpainting.")
        first_block_np = np.repeat(first_block_np, inpaint_C, axis=0)
        C = inpaint_C
        full_volume_pt = torch.zeros((1, C, total_depth, H, W), dtype=torch.float32, device="cpu")
    elif first_block_np.shape[0] != C:
        logger.warning(
            f"Channel mismatch: Base block has {first_block_np.shape[0]} channels, but expected {C}. Using base block channels."
        )
        C = first_block_np.shape[0]
        full_volume_pt = torch.zeros((1, C, total_depth, H, W), dtype=torch.float32, device="cpu")  # Recreate canvas

    full_volume_pt[0, :, :D, :, :] = numpy_to_pt(first_block_np)

    current_d = step
    for i in range(1, n_blocks):
        block_np = all_blocks_np[i]
        if block_np.ndim == 3:
            block_np = np.expand_dims(block_np, axis=0)  # (1, D, H, W)
        if block_np.shape[0] == 1 and C > 1:
            block_np = np.repeat(block_np, C, axis=0)
        elif block_np.shape[0] != C:
            logger.warning(
                f"Channel mismatch placing block {i}: got {block_np.shape[0]}, expected {C}. Skipping channel repeat."
            )

        block_pt = numpy_to_pt(block_np[:, ...])
        end_d = current_d + D
        full_volume_pt[0, :, current_d:end_d, :, :] = block_pt
        current_d += step

    full_volume_pt = full_volume_pt.to(device)
    torch.cuda.empty_cache()

    # inpainting seams using batch processing
    logger.info("Preparing junction information for batch inpainting")
    
    # Collect all junction information first
    junction_infos = []
    inpainting_base_seed = seed + n_blocks
    
    for i in range(n_blocks - 1):
        junction_idx = i + 1

        if use_gap_filling:
            # In gap mode, junction center should be in the middle of the gap
            # Gap starts at junction_idx * D + (junction_idx - 1) * gap_size = (junction_idx - 1) * step + D
            gap_start = (junction_idx - 1) * step + D
            junction_center_d = gap_start + gap_size // 2
        else:
            # In overlap mode, junction center is at the overlap point
            junction_center_d = junction_idx * step
        if use_gap_filling:
            process_region_size = max(gap_size * 3, 16)  # Ensure sufficient context around gap
        else:
            process_region_size = max(D, overlap * 3)  # Original overlap logic

        region_start_d = max(0, junction_center_d - process_region_size // 2)
        region_end_d = min(total_depth, region_start_d + process_region_size)
        region_depth = region_end_d - region_start_d

        if region_end_d > total_depth:
            region_start_d = max(0, total_depth - process_region_size)
            region_end_d = total_depth
            region_depth = region_end_d - region_start_d

        # Collect junction information for batch processing
        junction_info = {
            'junction_idx': junction_idx,
            'region_start_d': region_start_d,
            'region_end_d': region_end_d,
            'junction_center_d': junction_center_d,
            'region_depth': region_depth
        }
        
        if use_gap_filling:
            junction_info['gap_size'] = gap_size
            
        junction_infos.append(junction_info)
        
        logger.info(
            f"Prepared junction {i + 1}/{n_blocks - 1} from depth {region_start_d} to {region_end_d} (size: {region_depth})"
        )
    
    # Handle iterative inpainting fallback (not yet implemented for batch processing)
    if inpaint_iteratively:
        logger.warning("Iterative inpainting not yet implemented for batch processing. Falling back to single-pass batch inpainting.")
    
    # Perform batch inpainting on all collected junctions
    if junction_infos:
        full_volume_pt = batch_inpaint_junctions(
            full_volume_pt=full_volume_pt,
            junction_infos=junction_infos,
            inpainting_pipeline=inpainting_pipeline,
            num_inference_steps=num_inference_steps,
            device=device,
            args=args,
            seed=inpainting_base_seed,
            use_gap_filling=use_gap_filling,
            output_dir=output_dir,
            inpaint_iteratively=False,  # Not implemented for batch yet
            inpaint_iterations=inpaint_iterations,
            inpaint_region_size_ratio=inpaint_region_size_ratio,
            max_batch_size=20,  # Conservative batch size to avoid OOM
        )
    
    logger.info("Batch inpainting completed.")
    final_volume_np = pt_to_numpy(full_volume_pt.squeeze(0))  # Remove Batch dim

    if final_volume_np.shape[0] == 1:
        final_volume_np = final_volume_np.squeeze(0)  # (D, H, W)

    if threshold_value is not None:
        logger.info(f"Applying threshold at value {threshold_value}")
        final_volume_np = (final_volume_np > threshold_value).astype(np.float32)

    logger.info(f"Final stitched volume shape: {final_volume_np.shape}")
    return final_volume_np


def stitch_blocks_with_batch_inpainting(
    volumes,
    axis,
    overlap,
    inpainting_pipeline,
    device,
    output_dir,
    inference_steps,
    seed,
    binary=False,
    debug=False,
):
    """
    Stitch pre-generated blocks using the batch inpainting optimization.
    
    Args:
        volumes: List of volumes to stitch
        axis: Axis along which to stitch (0, 1, or 2)
        overlap: Overlap size between volumes
        inpainting_pipeline: Inpainting pipeline for seam fixing
        device: Device to use for computation
        output_dir: Output directory for debug files
        inference_steps: Number of inference steps for inpainting
        seed: Random seed
        binary: Whether to apply binary thresholding
        debug: Whether to save debug information
        
    Returns:
        Stitched volume as numpy array
    """
    if not volumes:
        return None
        
    # Convert volumes to pytorch tensors if needed
    volumes_pt = []
    for vol in volumes:
        if isinstance(vol, np.ndarray):
            vol_pt = numpy_to_pt(vol)
        else:
            vol_pt = vol
        
        # Ensure proper tensor format: add channel dimension if needed
        if len(vol_pt.shape) == 3:  # (D, H, W) -> (1, D, H, W)
            vol_pt = vol_pt.unsqueeze(0)
        
        volumes_pt.append(vol_pt)
    
    # Calculate output dimensions
    volume_shape = volumes_pt[0].shape
    axis_size = volume_shape[axis + 1]  # +1 because of channel dimension
    n_blocks = len(volumes_pt)
    
    # Calculate total size along the axis (gap-filling mode)
    total_axis_size = axis_size * n_blocks + overlap * (n_blocks - 1)
        
    # Create output dimensions
    output_shape = list(volume_shape)
    output_shape[axis + 1] = total_axis_size
    
    # Create full volume tensor
    full_volume_pt = torch.zeros(output_shape, device=device)
    
    # Place volumes into the full volume
    current_pos = 0
    for i, vol in enumerate(volumes_pt):
        vol = vol.to(device)
        
        if axis == 0:
            full_volume_pt[:, current_pos:current_pos + axis_size, :, :] = vol
        elif axis == 1:
            full_volume_pt[:, :, current_pos:current_pos + axis_size, :] = vol
        elif axis == 2:
            full_volume_pt[:, :, :, current_pos:current_pos + axis_size] = vol
        
        current_pos += axis_size + overlap  # Gap-filling mode: add gap between volumes
    
    # Add batch dimension if needed
    if len(full_volume_pt.shape) == 4:
        full_volume_pt = full_volume_pt.unsqueeze(0)
    
    # Create junction information for batch inpainting
    junction_infos = []
    for i in range(n_blocks - 1):
        # Gap-filling mode: junction center is in the middle of the gap
        gap_start = (i + 1) * axis_size + i * overlap
        junction_center = gap_start + overlap // 2
        process_region_size = max(overlap * 3, 16)
        region_start = max(0, junction_center - process_region_size // 2)
        region_end = min(total_axis_size, region_start + process_region_size)
        
        junction_infos.append({
            'junction_idx': i,
            'region_start_d': region_start,
            'region_end_d': region_end,
            'junction_center_d': junction_center,
            'region_depth': region_end - region_start,
            'gap_size': overlap,  # In case gap_filling mode is used
        })
    
    # Use batch inpainting if pipeline is available
    if inpainting_pipeline is not None and len(junction_infos) > 0:
        logger.info(f"Performing batch inpainting for {len(junction_infos)} junctions")
        
        # Create dummy args for batch inpainting
        dummy_args = argparse.Namespace(
            mask_type="gap_filling_compatible",
        )
        
        batch_inpaint_junctions(
            full_volume_pt=full_volume_pt,
            junction_infos=junction_infos,
            inpainting_pipeline=inpainting_pipeline,
            num_inference_steps=inference_steps,
            device=device,
            args=dummy_args,
            seed=seed,
            use_gap_filling=True,  # Should be True for gap_filling_compatible
            output_dir=output_dir,
            inpaint_iteratively=False,
            inpaint_iterations=3,
            inpaint_region_size_ratio=0.3,
            axis=axis,  # Pass the axis parameter
            max_batch_size=20,  # Conservative batch size to avoid OOM
        )
    
    # Convert back to numpy
    final_volume_np = pt_to_numpy(full_volume_pt.squeeze(0))
    
    if final_volume_np.shape[0] == 1:
        final_volume_np = final_volume_np.squeeze(0)
    
    if binary:
        final_volume_np = (final_volume_np > 0.5).astype(np.float32)
    
    logger.info(f"Final stitched volume shape: {final_volume_np.shape}")
    return final_volume_np
